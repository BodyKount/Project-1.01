window.onload = function() {
    let daySelectEl = document.getElementById("day");
    let muscleSelectEl = document.getElementById("muscle");
    let exerciseSelectEl = document.getElementById("exercise");
    for(let x in dropDown1) {
        daySelectEl.options[daySelectEl.options.length] = new Option(x, x);
    }

    daySelectEl.onchange = function() {
        //empty muscle and exercise dropdowns
        exerciseSelectEl.length = 1;
        muscleSelectEl.length = 1;
        //display default value
        for(let y in dropDown1[this.value]) {
            muscleSelectEl.options[muscleSelectEl.options.length] = new Option(y, y);
        }
    }
    
    muscleSelectEl.onchange = function() {
        //empty exercise dropdown
        exerciseSelectEl.length = 1;
        //display default value
        let z = dropDown1[daySelectEl.value][this.value];
        for(let i = 0; i < z.length; i++) {
            exerciseSelectEl.options[exerciseSelectEl.options.length] = new Option(z[i], z[i]);
        }
    }
}


